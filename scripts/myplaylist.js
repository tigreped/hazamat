/* Takes the track list and returns a randomized list of the tracks */
function randomList() {
    var amount = tracklist.length;
    var notpicked = new Array();
    var randomList = new Array();
    for (var i=0 ; i < amount ; i++)
        notpicked.push(i);
    while (notpicked.length > 0) {
        var arandom = notpicked[Math.floor((Math.random()*(notpicked.length-1))+0)];
        randomList.push(tracklist[arandom]);
        notpicked = removeItem(arandom, notpicked);
    }
    return randomList;
}

/* Remove the item from the list */
function removeItem(item, list) {
    var newArray = new Array();
    for (var i=0; i<list.length;i++) {
        if (list[i]!=item)
            newArray.push(list[i]);
    }
    return newArray;
}

